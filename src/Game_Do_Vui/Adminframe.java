    private void showTable(){
        List<users> userlist = Modify.GetUserList();
        for (users User : userlist) {
            usertable.addRow(new Object[] {User.getUserName(),User.getEmail(),
                                           User.getPassWord(),User.getRole(),User.getActive()});
        }
    }
