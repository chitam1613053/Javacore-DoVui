    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        String UserName=JOptionPane.showInputDialog(rootPane, "Insert the UserName");
        if (UserName != null){
        int a = JOptionPane.showConfirmDialog(rootPane, " do you sure delete this user");
        if(a == 0){
         Modify.DeleteUser(UserName);
         Modify.DeleteUserData(UserName);
         
         JOptionPane.showMessageDialog(rootPane, "User has been deleted"); 
         usertable.setRowCount(0);
         
         showTable();
        }}
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnActiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActiveActionPerformed
        int index=tblUsers.getSelectedRow();
        users user =  Modify.GetUserList().get(index);
        int check= JOptionPane.showConfirmDialog(rootPane,  "Do you want to active for "+user.getUserName()+"?");
        
        
        if(check==0){
            user.setActive(1);
            Modify.UpdateUser(user);
         
        usertable.setRowCount(0);
        showTable();
        }
    }//GEN-LAST:event_btnActiveActionPerformed

